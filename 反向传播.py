import torch

# ================================
# 1. 准备训练数据
# ================================

# 输入数据 x
x_data = [1.0, 2.0, 3.0]

# 对应的真实标签 y
# 真实关系是 y = 2x
y_data = [2.0, 4.0, 6.0]


# ================================
# 2. 定义模型参数
# ================================

# 创建一个Tensor作为模型参数 w
# 初始化 w = 1.0
w = torch.Tensor([1.0])

# 告诉 PyTorch：这个变量需要计算梯度
# 这样 PyTorch 才会自动记录计算过程
# 用于后面的反向传播
w.requires_grad = True


# ================================
# 3. 定义前向传播函数（模型）
# ================================

# 这里定义模型： y = w * x
def forward(x):
    return x * w


# ================================
# 4. 定义损失函数
# ================================

# 使用平方误差损失函数
# loss = (预测值 - 真实值)^2
def loss(x, y):
    y_pred = forward(x)  # 先计算预测值
    return (y_pred - y) ** 2  # 计算误差平方


# ================================
# 5. 训练前预测
# ================================

# 查看模型在训练前的预测效果
print("predict(before training)", 4, forward(4).item())

# item() 的作用：
# 将 PyTorch 的 Tensor 转换成普通 Python 数字
# 方便打印输出


# ================================
# 6. 开始训练模型
# ================================

# epoch 表示训练轮数
# 这里训练 100 轮
for epoch in range(100):

    # zip 会把 x_data 和 y_data 配对
    # (1,2) (2,4) (3,6)
    for x, y in zip(x_data, y_data):

        # 计算当前数据的损失
        l = loss(x, y)

        # ========================
        # 反向传播
        # ========================

        # 自动计算梯度
        # PyTorch 会根据计算图求出：
        # d(loss)/dw
        l.backward()

        # 输出当前梯度
        print('\tgrad:', x, y, w.grad.item())

        # ========================
        # 梯度下降更新参数
        # ========================

        # 更新公式：
        # w = w - 学习率 * 梯度
        # 这里学习率 = 0.01
        w.data = w.data - 0.01 * w.grad.data

        # ========================
        # 清空梯度
        # ========================

        # PyTorch 默认梯度是累加的
        # 如果不清零，会一直累加
        # 所以每次更新后要清空梯度
        w.grad.data.zero_()

    # 每一轮训练结束后输出当前 loss
    print("progress:", epoch, l.item())


# ================================
# 7. 训练后预测
# ================================

print("predict(after training)", 4, forward(4).item())